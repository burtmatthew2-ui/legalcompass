export function generateTemplateFile(templateKey: string): Blob | null {
  const template = templates[templateKey];
  if (!template) return null;

  return new Blob([template.content], { type: 'text/plain;charset=utf-8' });
}

export function downloadTemplate(templateKey: string, fileName: string) {
  const blob = generateTemplateFile(templateKey);
  if (!blob) {
    throw new Error("Template not found");
  }

  // Create download link
  const url = window.URL.createObjectURL(blob);
  const link = document.createElement('a');
  link.href = url;
  link.download = fileName.endsWith('.txt') ? fileName : `${fileName}.txt`;
  document.body.appendChild(link);
  link.click();
  
  // Cleanup
  setTimeout(() => {
    document.body.removeChild(link);
    window.URL.revokeObjectURL(url);
  }, 100);
}
